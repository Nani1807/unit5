let a = 10;
let b = 5;

console.log("a + b =", a + b); // Сложение
console.log("a - b =", a - b); // Вычитание
console.log("a * b =", a * b); // Умножение
console.log("a / b =", a / b); // Деление
console.log("a % b =", a % b); // Остаток от деления
console.log("a ** b =", a ** b); // Возведение в степень

